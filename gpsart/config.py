import os
import datetime

# 專案根目錄。
# 注意：本檔案位於 gpsart/ 子資料夾內，所以要往上跳一層才是專案根目錄，
# 否則輸出會跑到 gpsart/GPS_Results 這種錯誤位置。
# 輸出 (GPX/PNG) 固定寫在「主程式所在的資料夾」底下的 GPS_Results，
# 這樣不論從哪裡啟動、或整個資料夾被複製搬移，輸出都會跟著程式走。
try:
    BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
except NameError:
    BASE_DIR = os.getcwd()

OUTPUT_ROOT = os.path.join(BASE_DIR, "GPS_Results")

# [Fix 折返] 路口節點合併半徑 (公尺)。0 = 停用。
# 步行路網的路口常有多個相距幾公尺的節點 (行人穿越/路緣/人行道)，
# 會造成路徑在原地來回折返；合併後可減少折返。
# ⚠️ 目前「預設停用 (0)」：實測會造成路徑連到地圖邊界外、線條互相平行的異常，
#    原因尚未查明 (合併本身會把節點編號重編為 0..N 的小整數，疑似與其他流程衝突)。
#    待查明並修正前請保持 0。若要實驗可改成 10~15。
CONSOLIDATE_TOLERANCE_M = 0

# 下載時間估算 (依實測校準：0.27km²≈8.6s、2.3km²≈12s、15.1km²≈59s)
# 模型：固定連線開銷約 7 秒 + 每平方公里約 3.5 秒
DL_FIXED_OVERHEAD_S = 7.0

DL_SECONDS_PER_KM2 = 3.5

# [Fix 橫跳] A. 手繪線輕度平滑的容差 (公尺)。
# 手抖幾公尺，就會讓「每段找最近節點」在馬路兩側人行道之間反覆翻面 -> 橫跳。
# 10 = 輕度 (只消手抖，圖案幾乎不變)；20~25 = 中度 (連小彎都拉直)。0 = 不平滑。
SMOOTH_TOLERANCE_M = 10

# [Fix 橫跳] B. 人行道類道路的成本倍率 (住宅路 = 1.0)。
# 每條街兩側各有一條人行道，若成本太低，演算法會在兩側之間亂跳。
# 調高後會偏好「車道中心線」(一條街只剩一條線) -> 圖案乾淨很多。
# 1.5 = 原值 (容易橫跳)；2.5 = 偏好車道中心線；過高 (>4) 可能繞遠路。
FOOTWAY_PENALTY = 2.5

# [Fix 過馬路繞遠路] 行人穿越道(crossing)的成本倍率。
# 過馬路是必要動作且距離很短(實測平均約 10m)，若跟人行道一樣被懲罰，
# 路徑會為了避開過馬路而繞到很遠的路口，所以這裡維持 1.0 不懲罰。
# (需要有 footway 標籤才分得出來 -> 見 load_modules_in_background 的 useful_tags_way)
CROSSING_PENALTY = 1.0

# [Fix 繞遠路] 「已走過的路」再走一次的懲罰倍率。
# 原本是直接設 1e9 = 等於把走過的路封死，導致路徑回到附近時 (例如畫圓繞回起點、
# 或想過個馬路) 因為原路被封死而繞一大圈。
# 改成有限倍率：仍然強烈不鼓勵重複走，但「繞一大圈」比「重疊一小段」更貴時就會選擇重疊。
# 實測畫圓 (圓周2827m)：1e9 -> 6507m/偏離144m；8倍 -> 5552m；4倍 -> 4606m/偏離110m。
REUSE_PENALTY_FACTOR = 4.0

# [Fix 無意義折返] 「繞出去又折返回原地」的判定門檻。
# 現有的清理只抓「完全相同的節點」或「三點小尖刺(回到12m內)」，
# 抓不到「往前走 100 公尺再原路折返回來」這種較長的來回。
# 判定：實際走了 >= MIN_EXCURSION 公尺，最後卻回到出發點 RETURN_RADIUS 公尺內 -> 視為無意義折返，剪掉。
# 數字越小剪得越兇 (但可能剪掉圖案中「本來就要進去再出來」的細節)。
BACKTRACK_RETURN_RADIUS_M = 30.0

BACKTRACK_MIN_EXCURSION_M = 80.0

BACKTRACK_WINDOW = 60          # 往後最多檢查幾個節點

# 「細長度」門檻：圍出的面積 / 同周長圓的面積。
# 原路折返 ~= 0 (剪掉)；畫圓 = 1.0 (保留)。0.05 = 只剪掉又細又長的來回。
BACKTRACK_MAX_THINNESS = 0.05

# [Fix 橫跳] C. 沿紅線取樣的間距 (公尺)。每隔這麼遠就重找一次「最近節點」，
# 間距越小越貼合手繪線，但「翻面」的機會也越多 -> 橫跳越嚴重。
# 實測 (平滑10/人行道2.5)：40m -> 橫跳39、長度6161m；60m -> 橫跳33、長度4490m 且偏離未變差。
# 40 = 原值 (最貼合)；60 = 明顯減少橫跳 (建議先試)；過大可能讓尖角變鈍。
CORRIDOR_SAMPLE_STEP_M = 40

CORRIDOR_SAMPLE_STEP_RETRY_M = 20   # 精細重算模式用

